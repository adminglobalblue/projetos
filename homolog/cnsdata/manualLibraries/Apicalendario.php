    public function sanitizeString(string $s, $replaceSpace = true) {
		$s = strtoupper(preg_replace("/&([a-z])[a-z]+;/i", "$1", htmlentities(trim($s))));
		if ($replaceSpace) return str_replace(" ","_",$s);
		else return $s;        
    }
    
	public function request(int $year, string $state, string $city) {
        $state = $this->sanitizeString($state);
        $city = $this->sanitizeString($city);
		$url = "http://www.calendario.com.br/api/api_feriados.php?ano={$year}&estado={$state}&cidade={$city}&token={$this->token}";
		$ch = curl_init();
		curl_setopt($ch, CURLOPT_URL, $url);
		curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
		curl_setopt($ch, CURLOPT_CUSTOMREQUEST, "GET");
		curl_setopt($ch, CURLOPT_SSL_VERIFYHOST, 0);
		curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, 0);		
		curl_setopt($ch, CURLOPT_TIMEOUT_MS, 5000);
		curl_setopt($ch, CURLOPT_FOLLOWLOCATION, 1);
		$data['output'] = curl_exec($ch);
		$data['info'] = curl_getinfo($ch);
		$data['error'] = curl_error($ch);
		$data['nerror'] = curl_errno($ch);
		libxml_use_internal_errors(true);
		$data['xml'] = @simplexml_load_string($data['output']);
		curl_close($ch);
		return $data;
    }
	
	public function ifLocationExist(string $state, $city = "") {
        $state = $this->sanitizeString($state);
        if ($city) {
            $city = $this->sanitizeString($city);
            if (isset($this->list[$state])) {
                $listState = array_flip($this->list[$state]);
                return isset($listState[$city]);
            } return false;
        } else return isset($this->list[$state]);
    }
    
    public function getByState(string $state) {
        $state = $this->sanitizeString($state);
        if (isset($this->list[$state])) {
            return $this->list[$state];
        } else null;
    }
